#include "minitalk.h"

int	ft_atoi(char *str)
{
	int		i;
	int		sign;
	long	number;

	sign = 1;
	number = 0;
	i = 0;
	while (str[i] == ' ' || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] <= '9' && str[i] >= '0')
	{
		number = (number * 10) + (str[i] - 48) * sign;
		if (number > 2147483647)
			return (-1);
		if (number < -2147483648)
			return (0);
		i++;
	}
	return (number);
}

void	my_putnbr(int nbr)
{
	char	str[16];
	int		i;

	i = 0;
	while (nbr)
	{
		str[i] = nbr % 10 + 48;
		nbr = nbr / 10;
		i++;
	}
	while (i--)
		write(1, &str[i], 1);
}
